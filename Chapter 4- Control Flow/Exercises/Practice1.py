'''Q1. Write a python program with if statement that assigns 20 to the variable y and assigns 40 to the 
variable z if the variable '&' (ampersand) is greater than 100.'''

# Assuming '&' is your variable.
ampersand = 150 

# Using an if statement to check if '&' is greater than 100.
if ampersand > 100: 
    # If true, assign 20 to 'y' and 40 to 'z'.
    y = 20
    z = 40
    
    # If condition true, we assign the value 20 to the variable 'y'.
    print(y)
    # And assign the value 40 to the variable 'z'.
    print(z)