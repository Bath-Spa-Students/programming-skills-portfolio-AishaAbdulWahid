# Q3. Write a Python program that uses a loop to find the sum of all the numbers from 1 to 100.

# Initialize the sum
total = 0

# Using a for loop to iterate over the numbers from 1 to 100
for number in range (1, 101):
    # Add the current current number to the total
    total += number

    # Print the total
    print(total)
