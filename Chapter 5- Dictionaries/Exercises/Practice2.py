# Q2. Write a Python program to iterate through the keys of a dictionary and print them.
 # Create a dictionary to store information
my_self = {
    'name' : 'Aisha',
    'Age' : '18',
    'Occupation' : 'Cyber-security analyst',
    'Hobbies' : ['Reading', 'Going for a Run', 'Playing basketball', 'Coding'],
    'Location' : 'United Arab Emirates'
          }

# Iterate through the keys 
for key in my_self.keys():
    print(key)