'''Write a python program that stores fruit names in a list named fruits.\
   Unpack the list into three variables x, y and z and then print the values of each variable.'''

# Store fruit names in a list named fruits
fruits = ["dragon fruit", "orange", "apple"]

# Unpack the list into three variables x, y , and z.
x,y,z = fruits   # The first item in the list ("dragon fruit") is assigned to x.
                 # The second item in the list ("orange") is assigned to y.
                 # The third item in the list ("apple") is assigne to z.

# Print the values of each variable
print(f"The value of x is", x)   # Alternate line of code print(f"The value of x is {x}.")
print(f"The value of y is", y)   # Alternate line of code print(f"The value of y is {y}.")
print(f"The value of z is", z)   # Alternate line of code print(f"The valur of z is {z}.")
